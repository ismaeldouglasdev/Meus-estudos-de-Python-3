"""
Vamos criar uma função simples que soma dois números.
"""
def somar(a,b):
    resultado = a+b # soma os dois números
    return resultado # retorna o resultado
# usando a função
n1 = 33434
n2 = 99494
soma = somar(n1,n2) # chama a função e armazena o resultado
print(f"A soma de {n1} com {n2} é {soma}.") # exibe o resultado